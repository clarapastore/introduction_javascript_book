// Identify the data type for each of the following values:
// 'true'
// false
// 1.5
// 2
// undefined
// { foo: "bar" }

// To solve this, we can use the typeof operator

typeof("true");
// string

typeof(false);
// boolean

typeof(1.5);
// number

typeof(2);
// number

typeof(undefined);
// undefined

typeof({ foo: 'bar' })
// object

// Note: Since typeof is an operator, you don't need to wrap the values in parentheses.

